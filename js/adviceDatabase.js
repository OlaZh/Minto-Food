const SHOWN_ADVICE_KEY = 'shownAdvice';
const ADVICE_COOLDOWN_DAYS = 14; // Не показувати ту саму пораду 14 днів

/**
 * Отримати випадкову пораду з категорії, яку ще не бачили
 */
function getRandomAdvice(category) {
  const adviceList = ADVICE_DATABASE[category];
  if (!adviceList || adviceList.length === 0) return null;

  const shown = getShownAdvice();
  const now = Date.now();
  const cooldownMs = ADVICE_COOLDOWN_DAYS * 24 * 60 * 60 * 1000;

  // Фільтруємо поради, які ще не показували або показували давно
  const available = adviceList.filter((advice, index) => {
    const key = `${category}_${index}`;
    const lastShown = shown[key];
    return !lastShown || now - lastShown > cooldownMs;
  });

  // Якщо всі показані — беремо будь-яку
  const pool = available.length > 0 ? available : adviceList;
  const randomIndex = Math.floor(Math.random() * pool.length);
  const advice = pool[randomIndex];

  // Зберігаємо що показали
  const originalIndex = adviceList.indexOf(advice);
  markAdviceAsShown(category, originalIndex);

  return advice;
}

/**
 * Отримати показані поради з localStorage
 */
function getShownAdvice() {
  try {
    return JSON.parse(localStorage.getItem(SHOWN_ADVICE_KEY) || '{}');
  } catch {
    return {};
  }
}

/**
 * Позначити пораду як показану
 */
function markAdviceAsShown(category, index) {
  const shown = getShownAdvice();
  shown[`${category}_${index}`] = Date.now();

  // Очищаємо старі записи (більше 30 днів)
  const thirtyDaysAgo = Date.now() - 30 * 24 * 60 * 60 * 1000;
  for (const key in shown) {
    if (shown[key] < thirtyDaysAgo) {
      delete shown[key];
    }
  }

  localStorage.setItem(SHOWN_ADVICE_KEY, JSON.stringify(shown));
}

/**
 * Визначити категорію ІМТ
 */
function getBmiCategory(bmi) {
  if (bmi < 18.5) return 'bmi_underweight';
  if (bmi < 25) return 'bmi_normal';
  if (bmi < 30) return 'bmi_overweight';
  return 'bmi_obese';
}

/**
 * Визначити категорію прогресу
 */
function getProgressCategory(currentWeight, targetWeight, weightHistory) {
  if (!targetWeight || !currentWeight) return null;

  const diff = Math.abs(currentWeight - targetWeight);

  // Досягла цілі
  if (diff < 0.5) return 'progress_reached';

  // Майже ціль
  if (diff < 2) return 'progress_almost';

  // Аналізуємо історію за останні 2 тижні
  if (weightHistory && weightHistory.length >= 3) {
    const recent = weightHistory.slice(-3);
    const oldest = recent[0].weight;
    const newest = recent[recent.length - 1].weight;
    const trend = newest - oldest;

    // Якщо ціль — схуднути
    const wantsToLose = currentWeight > targetWeight;

    if (wantsToLose) {
      if (trend > 0.5) return 'progress_regress'; // Набрала замість скинути
      if (trend < -0.3) return 'progress_good'; // Є прогрес
      return 'progress_plateau'; // Стоїть
    } else {
      // Ціль — набрати
      if (trend < -0.5) return 'progress_regress'; // Скинула замість набрати
      if (trend > 0.3) return 'progress_good'; // Є прогрес
      return 'progress_plateau'; // Стоїть
    }
  }

  return null;
}

/**
 * Визначити категорію активності
 */
function getActivityCategory(activityLevel) {
  // activityLevel: 1.2, 1.375, 1.55, 1.725
  return activityLevel < 1.4 ? 'activity_low' : 'activity_high';
}

/**
 * Отримати персоналізовані поради (3 штуки)
 */
function getPersonalizedAdvice(userData) {
  const { bmi, goal, activityLevel, currentWeight, targetWeight, weightHistory } = userData;

  const advices = [];

  // 1. Порада по ІМТ або цілі
  const bmiCategory = getBmiCategory(bmi);
  const goalCategory = `goal_${goal || 'maintain'}`;

  // 50/50 шанс показати по ІМТ або по цілі
  const firstCategory = Math.random() > 0.5 ? bmiCategory : goalCategory;
  const firstAdvice = getRandomAdvice(firstCategory);
  if (firstAdvice) advices.push(firstAdvice);

  // 2. Порада по прогресу або активності
  const progressCategory = getProgressCategory(currentWeight, targetWeight, weightHistory);
  const activityCategory = getActivityCategory(activityLevel || 1.2);

  // Пріоритет регресу — показуємо м'які поради
  if (progressCategory === 'progress_regress') {
    const regressAdvice = getRandomAdvice('progress_regress');
    if (regressAdvice) advices.push(regressAdvice);
  } else if (progressCategory) {
    const progressAdvice = getRandomAdvice(progressCategory);
    if (progressAdvice) advices.push(progressAdvice);
  } else {
    const activityAdvice = getRandomAdvice(activityCategory);
    if (activityAdvice) advices.push(activityAdvice);
  }

  // 3. Мотиваційна порада
  const motivationAdvice = getRandomAdvice('motivation');
  if (motivationAdvice) advices.push(motivationAdvice);

  return advices;
}

