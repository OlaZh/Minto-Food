-- 2026-05-29
--
-- Збій/невдале зчитання баркоду іноді кешувало запис, де є лише
-- назва, а всі макро = 0. Такі рядки — сміття: вони перехоплюють
-- наступні сканування (локальний кеш повертається раніше за OFF)
-- і не дають отримати реальні дані.
--
-- "Порожнім" вважається запис, де kcal, protein, fat, carbs —
-- усі 0 або NULL (клітковина / цукор / сіль не враховуються).
--
-- ПОРАДА: спершу виконай SELECT нижче й переглянь, що саме піде
-- під видалення, і лише потім розкоментуй DELETE.
-- ============================================================

-- 1) Подивитися кандидатів на видалення:
SELECT barcode, name_ua, name_en, name_pl, kcal, protein, fat, carbs, source
FROM scanned_products
WHERE COALESCE(kcal, 0)    = 0
  AND COALESCE(protein, 0) = 0
  AND COALESCE(fat, 0)     = 0
  AND COALESCE(carbs, 0)   = 0;

